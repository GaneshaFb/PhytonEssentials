import math

pi = -3.14
x = 1
y = 2
z = 3

print(round(pi)) # Arredonda o número dentro dos () para o mais perto inteiro dele.
print(math.ceil(pi)) # Arredonda o número dentro dos () para o maior inteiro perto dele.
print(math.floor(pi)) # Arredonda o número dentro dos () para o menor inteiro perto dele.
print(abs(pi)) # Mostra a distância do número entre () até o 0 na reta númerica.
print(pow(pi,2)) # Calcula a potência usando o primeiro número como "base" e o segundo como "expoente".
print(math.sqrt(420)) # Calcula a raiz quadrada do número entre () e ele tem que ser positivo.
print(max(x,y,z)) # Mostra o maior número entre os números dos () separados por virgúlas.
print(min(x,y,z)) # Mostra o menor número entre os números dos () separados por virgúlas.
