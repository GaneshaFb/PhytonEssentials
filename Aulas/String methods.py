name = "Fabio Junior"

print(len(name)) # Imprimir o comprimento da str.
print(name.find("o")) # Imprimir a posição númerica da letra entre () na str.
print(name.capitalize()) # Imprimir a str com a primeira letra maiúscula.
print(name.upper()) # Imprimir a str toda em letras maiúsculas
print(name.lower()) # Imprimir a str toda em letras minúsculas.
print(name.isdigit()) # Checar se a str é um digito numérico.
print(name.isalpha()) # Checar se a str tem apenas letras do alfabeto.
print(name.count("o")) # Imprimir a quantas vezes a str entre () se repete na str.
print(name.replace("o","a")) # Imprimir a str substituindo a str entre "" para a outra após a vírgula.
print(name*3) # Imprimir a str multiplas vezes.
